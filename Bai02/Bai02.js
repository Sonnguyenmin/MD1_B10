let FirstName = "Nguyen";
// Hiển thị trên tap console
console.log(FirstName);
// Hiển thị lỗi trên tap console
console.error(FirstName);
//Hiển thị cảnh báo trên tap console
console.warn(FirstName);
//Xóa toàn bộ trên tab console
console.clear(FirstName);
//hiển thị thời gian trên tap console
console.time(FirstName);
//Hiển thị thời gian kết thúc trên tap console
console.timeEnd(FirstName);
//hiển thị bảng trên tap console
console.table([FirstName, 'First Name', 'Last Name']);
//Hiển thị hàm đếm trên tap console
console.count(FirstName);
//Hiển thị nhóm bắt đầu trên tap console
console.group(FirstName);
//Hiển thị nhóm kết thúc trên tap console
console.groupEnd(FirstName);

